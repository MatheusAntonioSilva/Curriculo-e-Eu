      <h2>Organização Sindical e Direito de Greve</h2>
                    <h3>União de empregados ou empregadores para a proteção dos direitos</h3>
                    <p>Os sindicatos estão presentes na Constituição Federal e têm o objetivo de reunir empregados 
                        ou empregadores que protegem seus interesses profissionais ou econômicos em relação ao serviço 
                        ou atividade que executam. Eles podem ser 'Patronais' com os empregadores e os profissionais 
                        formados pelos empregados. Na CLT, são encontradas as prerrogativas, os deveres e a contribuição 
                        sindical que é paga anualmente, de acordo com a atividade. Assim, existem também as 'Federações',
                        que são associações de sindicatos e 'Confederações', as associações de federações.
                        O direito de greve é assegurado a todo trabalhador, pela Constituição Federal. Por meio dela,
                        o empregado pode ter a oportunidade de reinvindicar por seus direitos e defender seus interesses.
                        O exercício da greve é legitimado, podendo haver suspensão coletiva da prestação de serviços,
                        temporariamente e de maneira pacífica, total ou parcial. Os grevistas podem persuadir ou aliciar 
                        os trabalhadores quanto à adesão a greve e também arrecadar fundos e ter livre divulgação do 
                        movimento. É proibido: violar ou constranger os direitos e garantias tanto do empregado, como do
                        empregador; a empresa não poderá frustrar a divulgação do movimento, muito menos coagir o 
                        empregado a comparecer ao trabalho; e a manifestação dos grevistas não poderá impedir o acesso ao
                        trabalho, nem ameaçar pessoas ou danificar a propriedade privada ou pública. </p>
                    <h2>Estabilidade</h2>
                    <p>O empregado tem garantia de seu emprego com a estabilidade, deixando de adquirir esse benefício se for demitido por justa causa. Esse tipo de estabilidade é encontrada nos concursos públicos. Atualmente, com a mudança na Constituição de 1988, surgiu a 'estabilidade provisória' que pode ser:
                        Dirigente sindical: quando o empregado for eleito a um cargo de direção ou representação 
                        sindical ou de associação profissional até um ano após o seu mandato. Exceto se cometer um erro grave.
                        Representante da CIPA: os eleitos não podem ser demitidos até um ano após o fim do mandato, 
                        exceto por motivo de disciplina, econômico, técnico ou financeiro.
                        Gestante: não poderá ser dispensada ou demitida, da confirmação da gravidez até cinco meses 
                        após o parto.
                        Acidente ou doença no trabalho: tem garantia de, no mínimo, doze meses, após término do auxílio-doença, 
                        independente do auxílio-acidente.
                        Normativa: dadas por acordos ou convenções coletivas de trabalho.</p>
                    <h2>Principais benefícios garantidos por lei ao empregado?</h2>
                    <h3>Seguro desemprego</h3>
                    <p>Por último, é um dos direitos do trabalhador receber o seguro-desemprego, quando ele é:
                        Demitido sem justa causa, com anotação em carteira de trabalho;
                        Se for trabalhador doméstico, receberá se o empregador recolher o FGTS;
                        Com pelo menos 6 meses de trabalho, antes de ser dispensado;
                        Sem condições de sustentar a família ou ele mesmo;
                        Não possui o benefício do INSS.
                        Esse auxílio é temporário e corresponde ao tempo de serviço do trabalhador. Se ele 
                        trabalhou de 6 a 11 meses, recebe 3 parcelas. De 12 a 23 meses, recebe 4 parcelas. De 24 a 
                        36 meses, recebe 5 parcelas. (Essa regra foi revogada no início de 2015)</p>
